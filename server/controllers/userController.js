// @desc    Upgrade to premium
// @route   PUT /api/users/premium
// @access  Private
exports.upgradeToPremium = asyncHandler(async (req, res, next) => {
  const { tier } = req.body;

  if (!['monthly', 'yearly', 'lifetime'].includes(tier)) {
    return next(new ErrorResponse('Invalid premium tier', 400));
  }

  let premiumExpiry = null;

  if (tier === 'monthly') {
    premiumExpiry = new Date(Date.now() + 30 * 24 * 60 * 60 * 1000); // 30 days
  } else if (tier === 'yearly') {
    premiumExpiry = new Date(Date.now() + 365 * 24 * 60 * 60 * 1000); // 365 days
  }

  const user = await User.findByIdAndUpdate(
    req.user.id,
    {
      isPremium: true,
      premiumTier: tier,
      premiumExpiry,
    },
    {
      new: true,
      runValidators: true,
    }
  );

  res.status(200).json({
    success: true,
    data: user,
  });
});

// @desc    Add service to favorites
// @route   PUT /api/users/favorites/:serviceId
// @access  Private
exports.addFavorite = asyncHandler(async (req, res, next) => {
  const user = await User.findById(req.user.id);

  // Check if already in favorites
  if (user.favorites.includes(req.params.serviceId)) {
    return next(new ErrorResponse('Service already in favorites', 400));
  }

  user.favorites.push(req.params.serviceId);
  await user.save();

  res.status(200).json({
    success: true,
    data: user,
  });
});

// @desc    Remove service from favorites
// @route   DELETE /api/users/favorites/:serviceId
// @access  Private
exports.removeFavorite = asyncHandler(async (req, res, next) => {
  const user = await User.findById(req.user.id);

  // Check if in favorites
  if (!user.favorites.includes(req.params.serviceId)) {
    return next(new ErrorResponse('Service not in favorites', 400));
  }

  user.favorites = user.favorites.filter(
    (fav) => fav.toString() !== req.params.serviceId
  );
  await user.save();

  res.status(200).json({
    success: true,
    data: user,
  });
});

// Helper function to get token from model, create cookie and send response
const sendTokenResponse = (user, statusCode, res) => {
  // Create token
  const token = user.getSignedJwtToken();

  const options = {
    expires: new Date(
      Date.now() + process.env.JWT_COOKIE_EXPIRE * 24 * 60 * 60 * 1000
    ),
    httpOnly: true
  };

  if (process.env.NODE_ENV === 'production') {
    options.secure = true;
  }

  res
    .status(statusCode)
    .cookie('token', token, options)
    .json({
      success: true,
      token
    });
}; 